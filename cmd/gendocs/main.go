// gendocs generates markdown documentation from the jr Cobra command tree.
// Usage: go run ./cmd/gendocs/... <outdir>
package main

import (
	"bytes"
	"encoding/json"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"
	"text/template"

	"github.com/sofq/jira-cli/cmd"
	"github.com/sofq/jira-cli/cmd/generated"
	jrerrors "github.com/sofq/jira-cli/internal/errors"
	"github.com/spf13/cobra"
	"github.com/spf13/pflag"
)

// ---- data model ----

// flagInfo holds extracted information about a single command flag.
type flagInfo struct {
	Name     string
	Type     string
	Required bool
	Default  string
	Usage    string
}

// verbInfo holds extracted information about a single verb (leaf command).
type verbInfo struct {
	Name    string
	Short   string
	Long    string // only set if different from Short
	Method  string
	Path    string
	HasBody bool
	Flags   []flagInfo
}

// resourcePage groups all verbs belonging to one top-level resource.
type resourcePage struct {
	Resource string
	Verbs    []verbInfo
	// SingleVerb is true when the resource has no subcommands (configure, raw, etc.)
	SingleVerb bool
}

// sidebarEntry is one entry in the VitePress sidebar JSON.
type sidebarEntry struct {
	Text string `json:"text"`
	Link string `json:"link"`
}

// exitCodeRow is one row in the error-codes table.
type exitCodeRow struct {
	ExitCode   int
	Name       string
	HTTPStatus string
	ErrorType  string
	Meaning    string
	Hint       string
}

// ---- schema lookup ----

type schemaKey struct {
	resource, verb string
}

func buildSchemaLookup() map[schemaKey]generated.SchemaOp {
	m := make(map[schemaKey]generated.SchemaOp)
	all := append(generated.AllSchemaOps(), cmd.HandWrittenSchemaOps()...)
	all = append(all, cmd.WatchSchemaOps()...)
	all = append(all, cmd.TemplateSchemaOps()...)
	all = append(all, cmd.DiffSchemaOps()...)
	for _, op := range all {
		m[schemaKey{op.Resource, op.Verb}] = op
	}
	return m
}

// ---- command tree walking ----

func extractFlags(c *cobra.Command) []flagInfo {
	var flags []flagInfo
	c.LocalFlags().VisitAll(func(f *pflag.Flag) {
		// Skip the automatically added --help flag.
		if f.Name == "help" {
			return
		}
		required := false
		if ann := f.Annotations[cobra.BashCompOneRequiredFlag]; len(ann) > 0 && ann[0] == "true" {
			required = true
		}
		flags = append(flags, flagInfo{
			Name:     f.Name,
			Type:     f.Value.Type(),
			Required: required,
			Default:  f.DefValue,
			Usage:    f.Usage,
		})
	})
	return flags
}

func buildVerbInfo(c *cobra.Command, schema map[schemaKey]generated.SchemaOp, resource string) verbInfo {
	vi := verbInfo{
		Name:  c.Name(),
		Short: c.Short,
		Flags: extractFlags(c),
	}
	if c.Long != "" && c.Long != c.Short {
		vi.Long = c.Long
	}

	op, ok := schema[schemaKey{resource, c.Name()}]
	if ok {
		vi.Method = op.Method
		vi.Path = op.Path
		vi.HasBody = op.HasBody
	}
	return vi
}

func walkCommands(root *cobra.Command, schema map[schemaKey]generated.SchemaOp) []resourcePage {
	var pages []resourcePage

	for _, sub := range root.Commands() {
		if sub.Hidden || sub.Name() == "help" || sub.Name() == "completion" {
			continue
		}
		resource := sub.Name()
		children := sub.Commands()

		// Filter out hidden / help children.
		var visible []*cobra.Command
		for _, ch := range children {
			if !ch.Hidden && ch.Name() != "help" && ch.Name() != "completion" {
				visible = append(visible, ch)
			}
		}

		if len(visible) == 0 {
			// Top-level leaf command — treat the command itself as the only verb.
			vi := buildVerbInfo(sub, schema, resource)
			pages = append(pages, resourcePage{
				Resource:   resource,
				Verbs:      []verbInfo{vi},
				SingleVerb: true,
			})
		} else {
			var verbs []verbInfo
			for _, ch := range visible {
				verbs = append(verbs, buildVerbInfo(ch, schema, resource))
			}
			pages = append(pages, resourcePage{
				Resource:   resource,
				Verbs:      verbs,
				SingleVerb: false,
			})
		}
	}

	// Sort for stable output.
	sort.Slice(pages, func(i, j int) bool {
		return pages[i].Resource < pages[j].Resource
	})
	return pages
}

